-- Remoção de tabelas
DROP TABLE IF EXISTS banco CASCADE;
DROP TABLE IF EXISTS agencia CASCADE;
DROP TABLE IF EXISTS cliente CASCADE;
DROP TABLE IF EXISTS ccorrente CASCADE;
DROP TABLE IF EXISTS tipo_transacao CASCADE;
DROP TABLE IF EXISTS cliente_transacoes CASCADE;


-- criação das tabelas
CREATE TABLE IF NOT EXISTS banco(
  numero INTEGER NOT NULL,
  nome VARCHAR(50) NOT NULL,
  ativo BOOLEAN NOT NULL DEFAULT TRUE,
  data_criacao TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY(numero)
);

-- criação das tabelas com relacionamento
CREATE TABLE IF NOT EXISTS agencia (
  banco_numero INTEGER NOT NULL,
  numero INTEGER NOT NULL,
  nome VARCHAR(100) NOT NULL,
  ativo BOOLEAN NOT NULL DEFAULT TRUE,
  data_criacao TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY(banco_numero, numero),
  FOREIGN KEY(banco_numero) REFERENCES banco(numero)
);


CREATE TABLE IF NOT EXISTS cliente (
  numero BIGSERIAL PRIMARY KEY,
  nome VARCHAR(100) NOT NULL,
  email VARCHAR(250) NOT NULL,
  ativo BOOLEAN NOT NULL DEFAULT TRUE,
  data_criacao TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);


CREATE TABLE IF NOT EXISTS ccorrente (
  banco_numero INTEGER NOT NULL,
  agencia_numero INTEGER NOT NULL,
  cliente_numero INTEGER NOT NULL,
  numero BIGINT NOT NULL,
  digito SMALLINT NOT NULL,
  ativo BOOLEAN NOT NULL DEFAULT TRUE,
  data_criacao TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY(banco_numero, agencia_numero, numero, digito, cliente_numero),
  FOREIGN KEY(banco_numero, agencia_numero) REFERENCES agencia(banco_numero, numero),
  FOREIGN KEY(cliente_numero) REFERENCES cliente (numero)
);


CREATE TABLE IF NOT EXISTS tipo_transacao (
  id SMALLSERIAL PRIMARY KEY,
  nome VARCHAR(50),
  ativo BOOLEAN NOT NULL DEFAULT TRUE,
  data_criacao TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS cliente_transacoes (
  id BIGSERIAL PRIMARY KEY,
  banco_numero INTEGER NOT NULL,
  agencia_numero INTEGER NOT NULL,
  conta_corrente_numero BIGINT NOT NULL,
  conta_corrente_digito SMALLINT NOT NULL,
  cliente_numero BIGINT NOT NULL,
  tipo_transacao_id SMALLINT NOT NULL,
  valor NUMERIC(15, 2) NOT NULL,
  data_criacao TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (
    banco_numero, agencia_numero, conta_corrente_numero, conta_corrente_digito,
    cliente_numero
  ) REFERENCES ccorrente (
    banco_numero, agencia_numero, numero, digito, cliente_numero
  )
);

